#include "../push_swap.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t max)
{
	size_t	i;

	i = 0;
	if (max != 0)
	{
		while ((i < (max - 1)) && src[i])
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (ft_strlen(src));
}
